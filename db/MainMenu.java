    private void readEditBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_readEditBtnActionPerformed
        UIManager.put("OptionPane.messageFont",new FontUIResource(new Font("Verdana", Font.BOLD, 13 )));
                               
            Object[] options= {"Yes", "Cancel"};
            int n = JOptionPane.showOptionDialog(null,
            "If you have already created a class file, you will be able to\n"+
            "navigate and select a class file for read/edit.  If you have not"+
            "\nalready created a class file, you should cancel from this form "+
            "\nand choose 'Create' from MAIN MENU."+
            "\nHave you already created a class file?",
            "Confirm New File Dialog",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE,
            null,
            options,
            options[1]);
        if (n==0) openReadEdit();
             
    }//GEN-LAST:event_readEditBtnActionPerformed

