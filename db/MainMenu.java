    //if the user confirms that they have saved a file, open the ReadEdit form.
    void openReadEdit(){
        readEdit=new ReadEditAdd();
        readEdit.setSize(frameDim);
        readEdit.setVisible(true);
        theDesktop.add(readEdit);
        //send MainMenu form to back. We may need it later.
        mainMenu.toBack();
        
    }
