    public void setFieldValues(String s[])throws IllegalArgumentException{
       if(s.length!=size)throw new IllegalArgumentException("Not the correct number of strings in the array");
        classNameLbl.setText((String)s[0]);
        teacherNameLbl.setText((String)s[1]);
        for(int i=0; i<fieldSize; i++){
            fields[i].setText(s[i+2]);
                
        }//for
        
        infoArea.setText(s[size-1]);
    
    } //end setFieldValues()
    
    //returns an array of Strings
    public String[] getFieldValues(){
        String values[]=new String[size];  //size=14
         
        //allocating the contents of classNameLbl to values[0], and teacherNameLbl to values[1].
        values[0]=(String)(classNameLbl.getText());
        values[1]=(String)(teacherNameLbl.getText());
        //allocating the 11 textBox values to values[2-12] in the loop
        for(int i=2; i<fieldSize+2; i++){
            values[i]=fields[i-2].getText(); 
            
        } //for     
               
        values[size-1]=infoArea.getText();  //add JTextArea to the array, final position.
        //values[size]=numZeroStr;
        return values;
    }  //end getFieldValues()
    
    
    //getFileName():  use the className to set the fileName (on saving) and
    //append underscore, and  
    //append the teacher name.
    String getFileName(){
        
        className=(String)(classNameLbl.getText());
        teacherName=(String)(teacherNameLbl.getText());
       
        StringBuilder fileName=new StringBuilder(className);
        fileName.append("\u005F");
        fileName.append(teacherName);
        
        return fileName.toString();
    }
    
