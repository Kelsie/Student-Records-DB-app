    private void Handler1(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Handler1
         
                     char c=evt.getKeyChar();
                     if(Character.isDigit(c)){
                            JOptionPane.showMessageDialog(null, "Error. \nNo digit allowed in name");
                        JTextField txtField= (JTextField)(evt.getSource());
                        txtField.setText("");
                        txtField.requestFocusInWindow();
                      }//if
               
    }//GEN-LAST:event_Handler1

